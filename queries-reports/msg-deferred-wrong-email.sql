SELECT e.Имя
, q."№ счета" 
,f.Предприятие
, c.ЕАдрес, c.ФИО, c.Телефон
FROM 
vwDeferredMsg q, Сотрудники e, Счета b, Предприятия f, Работники c
WHERE e.Менеджер = q."№ счета"/1000000
AND q.msg_status =997
AND q."№ счета" = b."№ счета"
AND b.Код = f.Код
AND b.КодРаботника = c.КодРаботника
AND (c.ЕАдрес !~ '([\w\-+]+(?:\.[\w\-+]+)*@(?:[\w\-]+\.)+[a-zA-Z]{2,7})' 
     OR c.ЕАдрес IS NULL)
ORDER BY e.ФИО, q."№ счета"
